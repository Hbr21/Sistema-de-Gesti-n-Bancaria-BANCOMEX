    private void pintarimagenB(JButton lbl, String ruta) {
        URL url=getClass().getResource(ruta);
        if(url!=null){
            this.imagen=new ImageIcon(url);
            this.icono=new ImageIcon(this.imagen.getImage().getScaledInstance(lbl.getWidth(), lbl.getHeight(),Image.SCALE_SMOOTH));
            lbl.setIcon(this.icono);
            lbl.setText("");
        }else{
            System.out.println("No se encontr√≥ la imagen"+ ruta);
        }
    }

